""" Module contains miscellaneous utility functions.
"""


def truncate(f, n):
    """ Truncates/pads a float f to n decimal places without rounding and
        returns a string """
    s = '{}'.format(f)
    if 'e' in s or 'E' in s:
        return '{0:.{1}f}'.format(f, n)
    i, p, d = s.partition('.')
    return '.'.join([i, (d+'0'*n)[:n]])
